package quiz;

public class Quiz1 {

	public static void main(String[] args) {
		
		// int형 변수 x 선언
		// x에 1을 저장
		// x의 값을 5로 변경
		// x를 출력

		int x;
		
		x = 1;
		x = 5; 
		System.out.println(x);
		
		// 변수 y 선언하고 바로 3을 대입
		// y의 값을 6으로 변경
		// y를 출력
		
		int y = 3;
		y = 6;
		System.out.println(y);
	}

}
